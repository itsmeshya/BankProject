package kotak.account.details;

public class ForLoop {

	public static void main(String[] args) 
		//for loop example
	{
		for(int times=0;times<10;times++) 
		{
			System.out.println("I will get a job");
		}
		//while loop example
		int times=0;
		while (times<5) {
			System.out.println("I am a quick learner");
			times++;}
		// do while loop example	
		int time=0;
		do {
			System.out.println("I am a hardworker");
			time++;
		} while (time<6);
		}
	}
